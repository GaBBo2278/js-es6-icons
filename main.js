let select = document.getElementById("select-type");
select.addEventListener("change", function () {

	let selezione = this.value;

	const filtrato = icons.filter(icons => {

		if (icons.type == selezione) {
			return true;
		}
		return false;
	});

	if(selezione == 'all'){
		generaIcone(icons, iconContainer);
	} else {
		generaIcone(filtrato, iconContainer);
	}

});

function generaIcone(icons, iconContainer) {
	let contenuto = "";
	icons.forEach(element => {
		contenuto += `<div class="icon">
						 <i style="color:${element.color}" class="${element.family} ${element.prefix}${element.name}"></i>
						<span>${element.name}</span>
					  </div>`;
	});
	iconContainer.innerHTML = contenuto;
}

